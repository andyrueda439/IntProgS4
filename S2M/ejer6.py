#Calcular edad actual y edad en 10 años
año_nacimiento = int(input("Ingrese su edad de nacimiento: "))
año_actual = int(input("Ingrese el año actual: "))
edad_actual = año_actual - año_nacimiento
edad_furura = edad_actual + 10

print(f"Edad actual: {edad_actual}")
print(f"Edad en 10 años: {edad_furura}\n")